export function Sidebar() {
  const pathname = usePathname();
  const { theme, setTheme, resolvedTheme } = useTheme();
  const [mounted, setMounted] = useState(false);

  useEffect(() => {
    setMounted(true);
  }, []);

  const handleThemeToggle = () => {
    if (!mounted) return;
    
    const root = document.documentElement;
    const currentTheme = resolvedTheme || theme || 'light';
    const newTheme = currentTheme === 'dark' ? 'light' : 'dark';
    
    // Set theme using next-themes
    setTheme(newTheme);
    
    // Also manually update the HTML class (same approach as ThemeToggle)
    if (newTheme === 'dark') {
      root.classList.add('dark');
      root.classList.remove('light');
    } else {
      root.classList.remove('dark');
      root.classList.add('light');
    }
  };

  const currentTheme = mounted ? (resolvedTheme || theme || 'light') : 'light';
  const isDark = currentTheme === 'dark';

  return (
    <aside className="fixed left-0 top-0 h-full w-64 dashboard-sidebar-bg border-r dashboard-sidebar-border flex flex-col z-40">
      {/* Logo */}
      <div className="h-16 flex items-center px-6 border-b dashboard-sidebar-border">
        <div className="flex items-center gap-3">
          <img 
            src="/logo.png" 
            alt="Mizu Pay" 
            className="w-20 h-20 object-contain"
          />
          <span className="font-semibold dashboard-text-primary text-lg">Mizu Pay</span>
        </div>
      </div>

      {/* Navigation */}
      <nav className="flex-1 px-4 py-6 space-y-1">
        {navItems.map((item) => {
          const isActive = pathname === item.href;
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`
                flex items-center gap-3 px-4 py-3 rounded-lg text-sm font-medium transition-all
                ${
                  isActive
                    ? 'bg-blue-50 dark:bg-blue-950/30 text-blue-600 dark:text-blue-400'
                    : 'dashboard-text-secondary dashboard-hover'
                }
              `}
            >
              {item.icon}
              <span>{item.name}</span>
            </Link>
          );
        })}
      </nav>

      {/* Theme Toggle */}
      <div className="px-4 py-4 border-t dashboard-sidebar-border">
        <button
          onClick={handleThemeToggle}
          className="w-full flex items-center justify-between px-4 py-2 rounded-lg text-sm font-medium dashboard-text-secondary dashboard-hover transition-colors"
        >
          <span className="flex items-center gap-2">
            {mounted && isDark ? (
              <>
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M20.354 15.354A9 9 0 018.646 3.646 9.003 9.003 0 0012 21a9.003 9.003 0 008.354-5.646z" />
                </svg>
                <span>Dark Mode</span>
              </>
            ) : (
              <>
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 3v1m0 16v1m9-9h-1M4 12H3m15.364 6.364l-.707-.707M6.343 6.343l-.707-.707m12.728 0l-.707.707M6.343 17.657l-.707.707M16 12a4 4 0 11-8 0 4 4 0 018 0z" />
                </svg>
                <span>Light Mode</span>
              </>
            )}
          </span>
        </button>
      </div>
    </aside>
  );
}

